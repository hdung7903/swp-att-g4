
package entity;

public class TypeApplication extends BaseEntity{
    private int id;
    private String name;

    public TypeApplication() {
    }

    public TypeApplication(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
